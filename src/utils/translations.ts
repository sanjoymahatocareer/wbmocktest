// Helper translation functions
export function getQuestionTranslation(qId: string, originalQuestion: any, language: 'EN' | 'BN') {
  if (language === 'EN') return originalQuestion;
  const translation = BENGALI_QUESTIONS_MAP[qId];
  if (translation) {
    return {
      ...originalQuestion,
      text: translation.text,
      options: translation.options,
      explanation: translation.explanation,
      subject: translation.subject
    };
  }
  return originalQuestion;
}

export function getTestTranslation(test: any, language: 'EN' | 'BN') {
  if (language === 'EN') return test;
  const translation = BENGALI_TESTS_MAP[test.id];
  if (translation) {
    const updatedQs = test.questions ? test.questions.map((q: any) => getQuestionTranslation(q.id, q, language)) : [];
    return {
      ...test,
      title: translation.title,
      examBoard: translation.examBoard || test.examBoard,
      topic: translation.topic || test.topic,
      questions: updatedQs
    };
  }
  return test;
}

export function translateUI(key: keyof typeof UI_TRANSLATIONS.EN, language: 'EN' | 'BN'): string {
  const transSet = UI_TRANSLATIONS[language];
  return (transSet[key] || UI_TRANSLATIONS.EN[key]) as string;
}
